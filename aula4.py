# Tipos int e float 
# int - números inteiros
# O tipo int representa qualquer numero
# Positivo ou negativo, int sem sinal é considerado
# positivo.
print(11) # int
print(-11) # int
print(0) # int
    

# float - números flutuantes
# O tipo float representa números reais, ou seja, números
# Positivos ou negativos com ponto flutuante.
# Float sem sinal é considerado positivo
print(1.1, 10.11)  # float
print(0.0, -1.5) # float

# A função type mostra o tipo que o Python 
# atribuiu para o valor
print(type('Otávio'))  
print(type(-1))   
print(type(1.1), type(-1.1), type(0.0))

